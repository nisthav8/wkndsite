function handleSearch() {
  const query = input.value.trim();
  if (query) {
    window.location.href = `https://www.google.com/search?q=${encodeURIComponent(query)}`;
  } 
}


input.addEventListener("keydown", function (event) {
  if (event.key === "Enter") {
    handleSearch(); 
  }
});


